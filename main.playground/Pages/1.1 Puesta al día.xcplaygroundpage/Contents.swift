import Foundation

/**

 - Como casi cualquier lenguaje, Swift tiene variables, funciones y sentencias
 - No es necesario terminar cada sentencia con ;
 - Aunque se puede poner, es más legible si no se pone.

 - Es necesario que el compilador conozca el tipo de cada variable y sentencia
 - Swift tiene los tipos básicos que el resto de lenguajes:

    - Tipos simples escalares, como Bool, Int o Double
    - Strings
    - Colecciones de datos como Arrays, Sets o Diccionarios (Mapas en Java, arrays asociativos en PHP)

 - ¿Cómo se define una variable?

 */

var yaSeSwift: Bool = false
// en Swift, tenemos los valores true y false como válidos para un booleano, como en Java
yaSeSwift = true

// En Swift, el compilador puede inferir el tipo de la mayoría de las variables
// Así es más cómodo y legible definir las variables

var unaCadena = "texto"
var unEntero = 10
var unFlotante = 3.14

